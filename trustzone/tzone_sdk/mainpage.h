/** @mainpage Open Virtualization
*
* <hr>
* Open Virtualization is a secure kernel that leverage's the TrustZone(R)
* architecture extensions in <a
* href="http://www.arm.com/products/processors/technologies/trustzone.php">ARM(R)</a>
* processors. The Open Virtualization kernel can be run in parallel with a more
* full-featured operating system, such as Linux, Android, or BSD, on the same 
* core. Open Virtualization uses security extensions to completely protect the
* secure kernel and peripherals from code running in the primary operating 
* system. This means that even if an attacker manages to obtain full supervisor 
* privileges to the primary operating system, the attacker cannot gain 
* access to the secure kernel.

*@section open_virtusation_highlights  Open Virtualization Highlights
*
* Open source software that is free to use and modify
*    Supports Android, Linux, and BSD operating systems
*    Small footprint that minimizes memory and system overhead
*    Available on ARM-11, Cortex-A9, and Cortex-A15 processors 
*
* <a href="http://www.sierraware.com/">Sierraware</a>, an embedded 
* virtualization company, developed and maintains the Open 
* Virtualization Project.
* <hr>
*
*/
